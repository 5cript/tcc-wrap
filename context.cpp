#include "context.hpp"

namespace TinyCompiler
{
//#####################################################################################################################
    Context::Context()
        : ctx_{nullptr}
    {
        reset();
    }
//---------------------------------------------------------------------------------------------------------------------
    void Context::setRuntime(std::string const& directory)
    {
    }
//---------------------------------------------------------------------------------------------------------------------
    Context::~Context()
    {
        tcc_delete(ctx_);
    }
//---------------------------------------------------------------------------------------------------------------------
    void Context::reset()
    {
        if (ctx_ != nullptr)
            tcc_delete(ctx_);
        ctx_ = tcc_new();

        setRuntime(runtime_);
    }
//#####################################################################################################################
}
